        if (idCiudadSeleccionada != 0) { 
            try {
                ciudadesNegocio.eliminar(idCiudadSeleccionada);
                JOptionPane.showMessageDialog(this, "Ciudad eliminada correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            } catch (NegocioException e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "No se encontró una ciudad para eliminar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
